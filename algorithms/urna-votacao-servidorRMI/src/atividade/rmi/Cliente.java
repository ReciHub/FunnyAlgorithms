    private void jButtonEnviarVotosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarVotosActionPerformed
        // TODO add your handling code here:
        String nome = jTextFieldNome.getText();
        int votos = Integer.valueOf(jTextFieldVotos.getText());
        
        try {
            if (stub.cadastrarVotos(nome, votos)){
                count++;
                jTextAreaCliente.append("Enviou votos com sucesso!\n");
                if (count == 1){
                    stub.contarVotos();
                }
            }
        } catch (RemoteException ex) {
            Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jTextFieldNome.setText("");
        jTextFieldVotos.setText("");
    }//GEN-LAST:event_jButtonEnviarVotosActionPerformed

